/**
 * Mock Telemetry Service
 * Simulates real-time fluctuations for vehicle speed, fuel level, fuel efficiency, and engine health.
 * Also dynamically moves the vehicles forward along their pre-determined routeCoordinates.
 */
export function getUpdatedTelemetry(vehicles: Vehicle[], tick: number): Vehicle[] {
  return vehicles.map((v) => {
    if (v.status === 'in_transit') {
      // 1. Simulate speed fluctuations (with realistic bounded variations around a base)
      const speedFluctuation = Math.floor(Math.sin(tick + v.plateNumber.charCodeAt(v.plateNumber.length - 1)) * 8);
      // Speed should range between 25 and 85 km/h for heavy/light trucks in transit
      const baseSpeed = v.type.includes('Light') ? 50 : 60;
      const currentSpeed = Math.max(25, Math.min(85, baseSpeed + speedFluctuation));

      // 2. Fuel level decays slowly (simulating consumption over ticks)
      // Since it's a mock, let's decrease it by 0.05% per tick, wrapping around 15% if it gets too low
      let fuelLevelPercent = v.fuelLevelPercent - 0.05;
      if (fuelLevelPercent < 15) {
        fuelLevelPercent = 95; // Simulates refuel!
      }
      fuelLevelPercent = Number(fuelLevelPercent.toFixed(2));

      // 3. Fuel efficiency (fuelConsumptionRate) fluctuations based on current speed and cargo weight
      const baseEfficiency = v.type.includes('Light') ? 10 : v.type.includes('Cooling') ? 16 : 28;
      // Faster speeds or refrigeration active can increase consumption slightly
      const speedFactor = (currentSpeed - 40) / 20;
      const tempFactor = v.tempCelsius !== undefined ? (v.tempCelsius < 0 ? 2.5 : 1.2) : 0;
      const efficiencyFluctuation = Math.sin(tick * 0.4 + v.plateNumber.charCodeAt(0)) * 1.2;
      const fuelConsumptionRate = Number(
        Math.max(6, Math.min(42, baseEfficiency + speedFactor + tempFactor + efficiencyFluctuation)).toFixed(1)
      );

      // 4. Engine status / health fluctuations (small jitter, with some random anomalies)
      const healthJitter = Math.floor(Math.sin(tick * 0.1 + v.plateNumber.charCodeAt(1)) * 2);
      let engineHealth = v.engineHealth + healthJitter;
      // Keep within realistic bounds unless already damaged
      if (v.id === 'veh-6') {
        engineHealth = Math.max(30, Math.min(50, engineHealth)); // maintenance bay truck
      } else {
        engineHealth = Math.max(78, Math.min(100, engineHealth));
      }

      // 5. Progressively update route index to move the vehicle across the Bangladesh Map
      // The tick determines progress. Every 12 ticks, progress the active index along the route coordinates.
      const ticksPerRouteSegment = 12;
      let currentRouteIndex = v.currentRouteIndex;
      if (v.routeCoordinates && v.routeCoordinates.length > 1) {
        currentRouteIndex = Math.floor((tick / ticksPerRouteSegment) % v.routeCoordinates.length);
      }

      // 6. Fluctuated refrigeration temperatures if it is a cold-chain vehicle (undergoing normal cycle)
      let tempCelsius = v.tempCelsius;
      if (tempCelsius !== undefined) {
        const tempFluctuation = Number((Math.sin(tick * 0.3 + v.id.charCodeAt(v.id.length - 1)) * 0.4).toFixed(1));
        tempCelsius = Number((tempCelsius + tempFluctuation).toFixed(1));
        // Bounded
        if (v.type.includes('Frozen') || v.tempCelsius < -10) {
          tempCelsius = Math.max(-20, Math.min(-14, tempCelsius));
        } else {
          tempCelsius = Math.max(2.5, Math.min(7.5, tempCelsius));
        }
      }

      return {
        ...v,
        currentSpeed,
        fuelLevelPercent,
        fuelConsumptionRate,
        engineHealth,
        currentRouteIndex,
        tempCelsius,
      };
    } else if (v.status === 'maintenance') {
      // Slow recovery of engine health in maintenance bay!
      const engineHealth = Math.min(100, v.engineHealth + (tick % 3 === 0 ? 1 : 0));
      return {
        ...v,
        currentSpeed: 0,
        engineHealth,
      };
    } else if (v.status === 'idle') {
      // Idle vehicles stay in place, speed is 0, but fuel level can slowly decay very slightly (0.005%)
      const fuelLevelPercent = Number(Math.max(5, v.fuelLevelPercent - 0.005).toFixed(2));
      return {
        ...v,
        currentSpeed: 0,
        fuelLevelPercent,
      };
    }

    return v;
  });
}

